class events {

    /**
     * Return event data for a quiz game enabled event.
     *
     * @param int $quizid The quiz ID.
     * @return array The event data array.
     */
    public static function quiz_game_enabled(int $quizid): array {
        return ['quizid' => $quizid, 'time' => time()];
    }
}
